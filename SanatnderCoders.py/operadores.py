# os operadores artmeticos são
'''
soma +
subtração -
multiplicação *
divisão /
divisão com inteiro //
resto da divisão %
potencia **
'''
numero1 = 10
numero2 = 20
print(numero1 + numero2)
print(numero1 - numero2)
print(numero1 * numero2)
print(numero1 / numero2)
print(numero1 // numero2)
print(2 ** 3)
print(2 % 3)

